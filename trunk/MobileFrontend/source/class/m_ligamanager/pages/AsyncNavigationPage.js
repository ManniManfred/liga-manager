

qx.Class.define("m_ligamanager.pages.AsyncNavigationPage",
{
	extend: qx.ui.mobile.page.NavigationPage,

	/*
	*****************************************************************************
	STATICS
	*****************************************************************************
	*/

	statics: {
	},


	/*
	* ****************************************************************************
	* CONSTRUCTOR
	* ****************************************************************************
	*/

	/**
	 * 
	 */
	construct: function(layout) {
		this.base(arguments, layout);
	},

	/*
	* ****************************************************************************
	* PROPERTIES
	* ****************************************************************************
	*/

	properties: {
	},

	/*
	 * ****************************************************************************
	 * EVENTS
	 * ****************************************************************************
	 */

	events:
	{
	},


	/*
	* ****************************************************************************
	* MEMBERS
	* ****************************************************************************
	*/

	members:
	{
		
		// overridden
		initialize : function()
		{
		  if (!this.isInitialized())
		  {
			this._initialize();
			this._startInitialize();
		  }
		},
		
		_startInitialize : function() {
			this._initDone();
		},
		
		_initDone : function() {
			this.__initialized = true;
			this.fireEvent("initialize");
		}
		

	}
});
